// ---- Language persistence ----
export function getLang() {
  return localStorage.getItem('4x4_lang') || 'ca';
}

export function setLang(lang) {
  localStorage.setItem('4x4_lang', lang);
}

export function t(key) {
  const lang = getLang();
  return (TRANSLATIONS[lang] && TRANSLATIONS[lang][key]) || TRANSLATIONS['ca'][key] || key;
}
